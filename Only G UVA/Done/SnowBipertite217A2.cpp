#define MAXN 110
using namespace std;

int N;
int Nar[MAXN][2];
int Used[MAXN]={0};
int Res;

void dfs(int k)
{
	Used[k]=1;

	for(int i=1;i<=N;i++)
		if(i!=k && !Used[i] )
		   if(Nar[k][0]==Nar[i][0] || Nar[k][1]==Nar[i][1])
		     {cout<<" Loop i = "<<i<<endl;dfs(i);}
}

int main()
{
	scanf("%d",&N);

	for(int i=1;i<=N;i++)
		scanf("%d%d",&Nar[i][0],&Nar[i][1]);

	for(int i=1;i<=N;i++)
		if(!Used[i])
		{
			cout<<"Main LOOp i = "<<i<<endl;
			dfs(i);
			Res++;
		}

	printf("%d\n",Res-1);
	return 0;
}
